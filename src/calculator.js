function sum(num1, num2) {
    return num1 + num2;
}

function subtr(num1, num2) {
    return num1 - num2;
}

function multi(num1, num2) {
    return num1 * num2;
}

function div(num1, num2) {
    return num1 / num2;
}

const num = 19;
const numm = 28;


console.log(sum(num, numm));
console.log(subtr(num, numm));
console.log(multi(num, numm));
console.log(div(num, numm));
